let currentLanguage: SupportedLanguage = 'en';

export function setCurrentLanguage(language: SupportedLanguage): void {
  currentLanguage = language;
}

export function getCurrentLanguage(): SupportedLanguage {
  return currentLanguage;
}

export function normalizeLanguagePreference(value: unknown): LanguagePreference {
  return LANGUAGE_PREFERENCES.includes(value as LanguagePreference)
    ? (value as LanguagePreference)
    : 'auto';
}

export function normalizeSupportedLanguage(value: unknown): SupportedLanguage {
  return SUPPORTED_LANGUAGES.includes(value as SupportedLanguage) ? (value as SupportedLanguage) : 'en';
}

export function resolveLanguage(preference: LanguagePreference, vscodeLanguage: string): SupportedLanguage {
  if (preference !== 'auto') {
    return preference;
  }

  const normalized = vscodeLanguage.toLowerCase();
  if (normalized === 'zh-cn' || normalized === 'zh' || normalized.startsWith('zh-hans')) {
    return 'zh-CN';
  }
  const primary = normalized.split(/[-_]/)[0];
  return SUPPORTED_LANGUAGES.includes(primary as SupportedLanguage) ? (primary as SupportedLanguage) : 'en';
}

export function getAntdLocale(language: SupportedLanguage) {
  return antdLocales[language];
}

export function t(
  language: SupportedLanguage,
  key: TranslationKey,
  params: Record<string, string | number> = {},
): string {
  const template = translations[language][key] ?? translations.en[key];
  return template.replace(/\{(\w+)\}/g, (match, name) =>
    Object.prototype.hasOwnProperty.call(params, name) ? String(params[name]) : match,
  );
}

export function createTranslator(language: SupportedLanguage) {
  return (key: TranslationKey, params?: Record<string, string | number>) => t(language, key, params);
}

export function translate(key: TranslationKey, params?: Record<string, string | number>): string {
  return t(currentLanguage, key, params);
}
